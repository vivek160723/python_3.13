# in Python, abstract classes are classes that cannot be instantiated directly and serve
# as a blueprint for other classes. They are designed to define methods that must be
# implemented in any derived class, ensuring a consistent interface.
#
# Key Features of Abstract Classes in Python:
# Defined Using ABC (Abstract Base Classes):
# Abstract classes are created by inheriting from the ABC class in the abc module.
# Abstract Methods:
# These are methods declared in the abstract class but without any implementation. Any subclass must override these methods.
# Cannot Be Instantiated:
# You cannot create an object of an abstract class.
# Concrete Methods:
# Abstract classes can also have concrete methods (methods with an implementation) that subclasses can inherit directly.

